// Helper function to get random images from categories
export const getRandomRestaurantImage = (category: string) => {
  const images = restaurantImages[category as keyof typeof restaurantImages] || restaurantImages.italian
  return images[Math.floor(Math.random() * images.length)]
}

export const getRandomFoodImage = (category: string) => {
  const images = foodImages[category as keyof typeof foodImages] || foodImages.pizza
  return images[Math.floor(Math.random() * images.length)]
}

export const getRandomOfferImage = () => {
  return offerImages[Math.floor(Math.random() * offerImages.length)]
}

export const getRandomAmbienceImage = () => {
  return ambienceImages[Math.floor(Math.random() * ambienceImages.length)]
}
